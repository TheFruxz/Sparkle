package de.moltenKt.paper.tool.annotation

/**
 * This code is starting a new sync runner
 * **NOTE, that this annotation is optional and only a helping indicator!**
 * @author Fruxz
 * @since 1.0
 */
annotation class RunningSync

/**
 * This code is starting a new async runner
 * **NOTE, that this annotation is optional and only a helping indicator!**
 * @author Fruxz
 * @since 1.0
 */
annotation class RunningAsync

/**
 * This code requires, to run inside a sync environment
 * **NOTE, that this annotation is optional and only a helping indicator!**
 * @author Fruxz
 * @since 1.0
 */
annotation class RequiresSync

/**
 * This code requires, to run inside a async environment
 * **NOTE, that this annotation is optional and only a helping indicator!**
 * @author Fruxz
 * @since 1.0
 */
annotation class RequiresAsync
